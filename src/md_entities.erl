%% expand_string([Char]) -> [Int]

%% replace &Name; with UnicodeChar if Name is a valid entity name

expand_entities_in_string(Str) -> expand_in_string(Str, []).

expand_in_string([$\\,H|T], L) ->
    %% escape characters are obeyed and passed through
    expand_in_string(T, [H,$\\|L]);
expand_in_string([$&|T], L) ->
    case collect_entity(T, []) of
	{Entity, T1} -> expand_in_string(T1, [Entity|L]);
	no           -> expand_in_string(T, [$&|L])
    end;
expand_in_string([H|T], L) -> expand_in_string(T, [H|L]);
expand_in_string([], L)    -> lists:reverse(L).

collect_entity([$;|T], L) ->
    E = lists:reverse(L),
    case entity(E) of
	no -> 
	    io:format("** warning ~p is not a valid entity~n",[E]),
	    no;
	N  ->
	    {N, T}
    end;
collect_entity([$\n|_], _) -> no;
collect_entity([H|T], L)   -> collect_entity(T, [H|L]);
collect_entity([], _)      -> no.



